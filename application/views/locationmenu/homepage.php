<h1>Faculty of Management</h1>
<p> Faculty of Management and Finance is one of seven faculties of the University of Colombo. It conducts both undergraduate and post-graduate degree programs.It traces its roots to the establishment of the Department of Commerce and Management Studies which was later upgraded to a faculty made up of six academic departments. Studies into accounting, business economics, finance, human resource management, international business, management, organization studies and marketing. Its Postgraduate and Mid-career Development Unit conducts the highly recognized University of Colombo MBA program. This Master of Business Administration program was the first MBA program to be established in Sri Lanka.</p>
<center><img src="<?php echo base_url(); ?>templates/images/MANAGEMENT.jpg" width=970px height=504px></center>

<h1>Faculty of Arts</h1>
<p> The largest faculty in of the University of Colombo, it is made up of eight academic departments and several teaching units, it undertakes studies and research in areas of humanities and social sciences. The origins of the faculty dates back to 1921, with the establishment of the Ceylon University College, as courses in arts subjects where the first to be started. Upgraded to a faculty in 1942 with the establishment of the University of Ceylon that year, it was moved completely to Peradeniya in the early 1950s. In 1963, a new arts faculty was established in Colombo as part of the University of Ceylon, Colombo campus which continues to function as the current faculty. the first Dean of the Faculty of Arts was Prof. Hem Chandra Ray, who was called out of retirement in India. He was former Dean at Peradeniya University.</p>
