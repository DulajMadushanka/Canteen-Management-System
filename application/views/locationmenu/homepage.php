<h1>Faculty of Science</h1>
<p> Faculty of Science (also known as The Science Faculty) is one of seven faculties of the University of Colombo. The oldest Faculty of Science in the country, it conducts both undergraduate and post-graduate degree programs in many fields of science.

Landmark building of Department of Mathematics, Faculty of Science, University of Colombo

Established in 1942 with the founding the University of Ceylon, as such it is one of the two oldest faculties in the island. At its formation its nucleus was formed from staff from the Science Section of the Ceylon Technical College. Part of the faculty was moved to Peradeniya, with other department remaining in Colombo. The faculty carries out research and studies Chemistry, Mathematics, Nuclear science, Physics, Plant Sciences, Statistics and Zoology.</p>
<center><img src="<?php echo base_url(); ?>templates/images/SCIENCE.jpg" width=970px height=504px></center>

<h1>Faculty of Law</h1>
<p>The Faculty of Law (also known as The Law Faculty) is one of seven faculties of the University of Colombo. Being the premier law school in the country, and the only Faculty of Law in the entire University system of the country it conducts both undergraduate and post-graduate degree programs in the field law.
Established in 1947 in the University of Ceylon as the department of law it was shortly moved to Peradeniya as part of the Faculty of Arts. However the department was later shifted to the Colombo Campus of the University of Ceylon upgrade to a Faculty of Law in 1968. The faculty maintains close links with the Colombo Law College, which conducts the law exams need for admittance as an attorney at law.</p>
