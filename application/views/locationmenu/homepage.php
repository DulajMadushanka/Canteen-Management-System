<h1>University of Colombo School of Computing</h1>
<p>The University of Colombo School of Computing (UCSC) is a higher educational institute affiliated to the University of Colombo in Sri Lanka providing Computer Science and Information and Communication Technology education. The UCSC was formed on 1 September 2002 by bringing together the Department of Computer Science of the Faculty of Science of the University of Colombo and the Institute of Computer Technology and considered as the leading computing higher educational institution in Sri Lanka.</p>
